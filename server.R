    temp=get.data.set()
    input$new.column
    input$add_column
    isolate({
      if(!is.null(get.data.set())&&!is.null(input$new.column)){
          colu = strsplit(input$new.column,"\n",fixed=T)[[1]]
          if(length(colu)==1){
